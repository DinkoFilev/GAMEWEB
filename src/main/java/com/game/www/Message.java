package com.game.www;

public class Message {

	private String from;
	private String text;

	public void setFrom(String from) {
		this.from = from;

	}

	public String getFrom() {
		return this.from;
	}

	public void setText(String text) {
		this.text = text;

	}

	public String getText() {
		return this.text;
	}
}
